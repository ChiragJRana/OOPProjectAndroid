package com.example.splash_screen;

public class UserProfile {
    public String FName;
    public String LName;
    public String Email;
    public String std;
    public String DOB;

    public UserProfile(String FName, String LName, String email, String std, String DOB) {
        this.FName = FName;
        this.LName = LName;
        this.Email = email;
        this.std = std;
        this.DOB = DOB;
    }
}
